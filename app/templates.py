def search_found_msg(count: int) -> str:
    """
    –°–∫–ª–æ–Ω—è–µ—Ç —Å–ª–æ–≤–∞ –≤ SEARCH_FOUND_MSG –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞
    –º–∞—Ç–µ—Ä–∏–∞–ª–æ–≤ (–∏–º–µ–Ω–∏—Ç–µ–ª—å–Ω—ã–π –ø–∞–¥–µ–∂)
    :param count: –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –º–∞—Ç–µ—Ä–∏–∞–ª–æ–≤
    :return: —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–Ω–∞—è —Å—Ç—Ä–æ–∫–∞
    """
    remainder = count % 10

    if 11 <= count <= 19 or remainder == 0:
        noun = '–º–∞—Ç–µ—Ä–∏–∞–ª–æ–≤'
    elif remainder == 1:
        noun = '–º–∞—Ç–µ—Ä–∏–∞–ª'
    elif 2 <= remainder <= 4:
        noun = '–º–∞—Ç–µ—Ä–∏–∞–ª–∞'
    else:
        noun = '–º–∞—Ç–µ—Ä–∏–∞–ª–æ–≤'

    msg = SEARCH_FOUND_MSG.format(count, noun)

    if count > 1:
        msg += ' ' + SEARCH_FOUND_MANY_MSG

    return msg


